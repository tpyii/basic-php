function translate($str = '')
{
  $out = '';
  $alfabet = [
    'a' => 'a',  'б' => 'b',  'в' => 'v',
    'г' => 'g',  'д' => 'd',  'е' => 'e',
    'ё' => 'e',  'ж' => 'zh',  'з' => 'z',
    'и' => 'i',  'й' => 'y',  'к' => 'k',
    'л' => 'l',  'м' => 'm',  'н' => 'n',
    'о' => 'o',  'п' => 'p',  'р' => 'r',
    'с' => 's',  'т' => 't',  'у' => 'u',
    'ф' => 'f',  'х' => 'h',  'ц' => 'c',
    'ч' => 'ch',  'ш' => 'sh',  'щ' => 'sch',
    'ь' => '\'',  'ы' => 'y',  'ъ' => '\'',
    'э' => 'e',  'ю' => 'yu',  'я' => 'ya'
  ];

  foreach (mb_str_split($str) as $chr) {
    if (array_key_exists(mb_strtolower($chr), $alfabet)) {
       $out .= (mb_strtoupper($chr) === $chr) 
              ? ucfirst($alfabet[mb_strtolower($chr)]) 
              : $alfabet[$chr];
    } else {
      $out .= $chr;
    }
  }

  return $out;
}

