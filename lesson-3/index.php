function menu($menu = [])
{
  $out = '<ul>';

  foreach ($menu as $title => $link) {
    $out .= is_array($link) 
              ? menu($link) 
              : "<li><a href='$link'>$title</a></li>";
  }

  $out .= '</ul>';

  return $out;
}
